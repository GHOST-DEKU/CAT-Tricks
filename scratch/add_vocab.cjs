const words = rawText.split(/\n\n+/);
const result = [];

words.forEach(block => {
    block = block.trim();
    if (!block) return;
    if (block.includes("(Note: This document contains a highly detailed selection")) return;

    const lines = block.split('\n');
    let title = "";
    let pronunciation = "";
    let partOfSpeech = "";
    let meaning = "";
    let synonyms = "";
    let origin = "";
    let memoryAssociation = "";
    let example = "";
    let swapTest = "";

    lines.forEach(line => {
        if (!line.includes(': ')) {
            if (!title) title = line.trim();
        } else {
            const splitIndex = line.indexOf(': ');
            const key = line.substring(0, splitIndex).trim();
            const val = line.substring(splitIndex + 2).trim();

            if (key === 'Pronunciation') pronunciation = val;
            if (key === 'Part of Speech') partOfSpeech = val;
            if (key === 'Meaning') meaning = val;
            if (key === 'Synonyms') synonyms = val;
            if (key === 'Origin') origin = val;
            if (key === 'Memory Association') memoryAssociation = val;
            if (key === 'Example') example = val;
            if (key === 'Swap Test') swapTest = val;
        }
    });

    if (!title) return;

    let tag = partOfSpeech + "s";
    if (partOfSpeech === "Noun / Verb (Gerund)") tag = "Nouns";
    if (partOfSpeech === "Adverb") tag = "Adverbs";

    let topic = "";
    const firstLetter = title[0].toUpperCase();
    if (firstLetter >= 'A' && firstLetter <= 'D') topic = "A-D";
    else if (firstLetter >= 'E' && firstLetter <= 'H') topic = "E-H";
    else if (firstLetter >= 'I' && firstLetter <= 'P') topic = "I-P";
    else if (firstLetter >= 'Q' && firstLetter <= 'Z') topic = "Q-Z";

    let finalSwapTest = swapTest;
    const swappedWordMatch = example.match(/\((.*?);\s*(.*?)\)/);
    if (swappedWordMatch && swappedWordMatch[2]) {
        const replacement = swappedWordMatch[2].trim();
        if (replacement) {
            try {
                finalSwapTest = swapTest.replace(new RegExp("\\b" + replacement + "\\b", 'i'), "<em>" + replacement + "</em>");
            } catch(e) {}
        }
    } else {
        const syns = synonyms.split(',').map(s=>s.trim());
        for (let s of syns) {
            if (swapTest.toLowerCase().includes(s.toLowerCase())) {
                try {
                    finalSwapTest = swapTest.replace(new RegExp("\\b" + s + "\\b", 'i'), "<em>" + s + "</em>");
                    break;
                } catch(e) {}
            }
        }
    }
    
    if (!finalSwapTest.includes('<em>') && swappedWordMatch && swappedWordMatch[2]) {
         finalSwapTest = swapTest.replace(swappedWordMatch[2], "<em>" + swappedWordMatch[2] + "</em>");
    }
    
    const trickObj = {
        dateAdded: new Date("2026-06-28T14:00:00.000Z").toISOString(),
        section: "Vocab",
        title: title,
        tags: [tag],
        module: "Vocab",
        topic: topic,
        whatItIs: meaning,
        whereToUse: pronunciation,
        logic: "<strong>Origin:</strong> " + origin + "<br><strong>Memory Trick:</strong> " + memoryAssociation,
        formula: synonyms,
        example: example + "<br><strong>Swap test:</strong> " + finalSwapTest,
        icon1: "📖",
        icon2: "🗣️",
        icon3: "🧠",
        icon4: "📚",
        icon5: "📝"
    };
    
    result.push(trickObj);
});

